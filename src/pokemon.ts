/****************************************************************************************************/

class Pokemon
{
  name: string
  health: number
  attack: number
  defense: number
  speed: number
  specialDefense: number
  specialAttack: number

  /**************************************************/

  constructor(name: string, health: number, attack: number, defense: number, speed: number, specialDefense: number, specialAttack: number)
  {
    this.name = name;
    this.health = health;
    this.attack = attack;
    this.defense = defense;
    this.speed = speed;
    this.specialDefense = specialDefense;
    this.specialAttack = specialAttack;
  }

  /**************************************************/

  
}

/****************************************************************************************************/

export { Pokemon };

